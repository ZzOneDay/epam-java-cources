package com.epam.university.java.core.task015;


public class PointImpl implements Point {
    private double x;
    private double y;

    PointImpl(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
